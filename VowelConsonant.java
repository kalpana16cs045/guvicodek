class VowelConsonant {

    public static void main(String[] args) {

        char ch = 'e';

        if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' ) 
        {
            System.out.println(ch + " the is vowel");
        }
        else
        {
            System.out.println(ch + " the is consonant");
        }

    }
}
